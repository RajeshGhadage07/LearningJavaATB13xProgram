package Operators_in_JAVA;

public class Airthmatic_operator {
    static void main(String[] args) {
//        + ---- Addition
//        - ---- Subtraction
//        * ---- Multiplication
//        / ---- Division
        int a = 5;
        int b = 9;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

    }

}
